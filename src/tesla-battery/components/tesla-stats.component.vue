<template>
  <div class="tesla-stats">
    <ul>
      <li v-for="stat in stats"
          :key="stat.model">
        <div :class="'tesla-stats-icon tesla-stats-icon--'+stat.model |  lowercase"></div>
        <p>{{stat.miles}}
          <span>{{unit}}</span>
        </p>
      </li>
    </ul>
  </div>
</template>

<script>
export default {
  name: 'tesla-stats',
  props: {
    stats: {
      type: Array,
      required: true,
    },
    unit: {
      type: String,
      required: true,
    },
  },
  filters: {
    lowercase(value) {
      return !value ? '' : value.toLowerCase();
    },
  },
};
</script>
